CREATE TABLE CHITIETHD (
	MASP VARCHAR(10),
    MAHD VARCHAR(10),
    SOLUONGSP INT ,
    DONGIA DECIMAL(18,0),
	TONGTIEN DECIMAL(18,0),
	CONSTRAINT PK_CHITIETHD PRIMARY KEY(MASP, MAHD)
);
--Bảng Phiếu Nhập
CREATE TABLE PHIEUNHAP(
	MAPN VARCHAR(10) NOT NULL,
	NGAYNHAP DATE,
