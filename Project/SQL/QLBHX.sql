    CONSTRAINT PK_PHIEUNHAPHANG PRIMARY KEY (MAPN)
);
-- Bảng Chi Tiết Phiếu Nhập
CREATE TABLE CHITIETPN (
    MAPN VARCHAR(10) NOT NULL,
    MASP VARCHAR(10) NOT NULL,
